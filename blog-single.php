            <h2 class="mb-3">Instalación de Paneles Solares</h2>
            <h6>Usted ya puede instalar paneles solares en casa</h6>
            <p>Así lo contempla el decreto 0570 de 2018 que permite la entrada de energías renovables a la matriz energética del país</p>
            <h3>¿Qué energía usar?</h3>
            <p>La solar, aunque es la más conocida, no es la única opción. Las energías eólica y mareomotriz (según el lugar) también pueden ayudar. En Europa, por ejemplo, se ha popularizado el uso de pequeñas turbinas eólicas capaces de abastecer a edificios enteros. Sin embargo, este sistema puede generar ruidos y molestias, por eso se usa sobre todo en espacios rurales y en combinación con energía solar.
            <br>
            La energía mareomotriz, que aprovecha el poder de las mareas para generar energía a través de turbinas y otros mecanismos subacuáticos, requiere un desarrollo tecnológico significativo para usarlo en menor escala. Sí, hay casas flotantes que funcionan con esta tecnología, pero su instalación es costosa, y necesitarán de la ayuda de otra energía complementaria.
            <br>
            Si no vive en el campo o cerca del mar, y prefiere seguir el camino tradicional y más fiable, entonces lo mejor es la energía solar. Tiene más fácil acceso en el ámbito urbano y es la más popular de las renovables. En Colombia funciona bien incluso en las regiones lluviosas. Como el país queda en la zona ecuatorial recibe una radiación solar equitativa a lo largo del año. Esta es la opción más viable en el país por su practicidad y eficiencia.</p>
