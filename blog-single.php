            <p>Un HotSpot WiFi, es un equipo de acceso que ofrece cobertura wireless (sin cables) mediante estándares WiFi, con el objetivo de establecer una comunicación inalámbrica WiFi entre un dispositivo "cliente".
            <br>
            Una de las características a destacar de un HotSpot es un portal cautivo que aparece al abrir el explorador y que habitualmente solicita una identificación mediante User y password.</p>

