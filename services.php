                    <p>El cableado estructurado consiste en el tendido de cables en el interior de un edificio, con el propósito de implantar en un futuro una red de área local. Suele tratarse de cable de par trenzado de cobre UTP/STP, para redes de tipo IEEE 802.3</p>
