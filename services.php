                      <li><span class="fa fa-check"></span>Categoría 3: se utiliza en redes 10BaseT y puede transmitir datos a velocidades de hasta 10 Mbit/s.e</li>
                      <li><span class="fa fa-check"></span>Categoría 4: se utiliza en redes Token Ring y puede transmitir datos a velocidades de hasta 16 Mbit/s.</li>
                      <li><span class="fa fa-check"></span>Categoría 5: puede transmitir datos a velocidades de hasta 100 Mbit/s.</li>
                      <li><span class="fa fa-check"></span>Categoría 6: Redes de alta velocidad hasta 1 Gbit/s.</li>
                      <li><span class="fa fa-check"></span>Categoría 6A: Redes de alta velocidad hasta 10 Gbit/s</li>
